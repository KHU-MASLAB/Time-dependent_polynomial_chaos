function y = hermite_F(n,x)

y = zeros(1,11);
y(1) = 1;
y(2) = x;
y(3) = x^2 - 1;
y(4) = x^3 - 3*x;
y(5) = x^4 - 6*x^2 + 3;
y(6) = x^5 - 10*x^3 + 15*x;
y(7) = x^6 - 15*x^4 + 45*x^2 - 15;
y(8) = x^7 - 21*x^5 + 105*x^3 - 105*x;
y(9) = x^8 - 28*x^6 + 210*x^4 - 420*x^2 + 105;
y(10) = x^9 - 36*x^7 + 378*x^5 - 1260*x^3 + 945*x;
y(11) = x^10 - 45*x^8 + 630*x^6 - 3150*x^4 +4725*x^2 - 945;

y = y(1:n+1);